#Create and call the function, which calsulates an area of rectangle by formula (s=1/2*a*b).
def area(side_a, side_b):
    return (side_a*side_b)/2

#Create the function, which dependence on the formal parameter value "TYPE"(True/False) calculates an area of rectangle or a perimeter of rectangle.
def rectangle(TYPE, side_a, side_b):
    return area(side_a, side_b) if TYPE else 2*(side_a + side_b)

#Create the function that finds a maximum value from a list of value.
def maximum_value(list_):
    value = 0
    for i in list_:
        if (value<i): value = i
    return value

#Create the function that multiplication of values of elements of list.
def multiplication(list_):
    result = 1
    for i in list_:
        result *= i
    return result