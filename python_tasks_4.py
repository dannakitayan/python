#Match the following English characters with Russian letters: "h – х, e – е, l – л, o – о, w – в, r – р, d – д",
#and convert the string "hello world!" into Russian symbols. 
def first(string_, list1, list2):
    merge = dict(zip(list1, list2))
    for i in string_:
        for j in merge:
            if (i == j):
                string_ = string_.replace(i, merge[j])
    return string_

