#Write the recursive function that calculates the factorial of number n.
def factorial(n):
    if (n == 0):
        return 1
    else:
        return n*factorial(n-1)

#Write the function that calculates the arithmetic mean of arguments.
def arithmetic_mean(*args):
    result = 0
    for i in args:
        result += i
    return result/len(args)

#Release the function that sorts the selected elements ascending order: the elements get from list, which choose with selector-function.
def ascending_sort(list_):
    for i in range(len(list_)):
        for j in range(len(list_)):
            if (list_[i] < list_[j]):
                list_[i], list_[j] = list_[j], list_[i]
    return list_

def sort_function(list_, selector):
    new_list = []
    for i in list_:
        if selector(i):
            new_list.append(i)
    return ascending_sort(new_list)

